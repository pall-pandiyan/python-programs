import math

# round method have two parameters the first is the number to be rounded, the second optional parameter is limitation of rounding.
print("round(math.pi, 2):", round(math.pi, 2))
print("round(math.pi, 4):", round(math.pi, 4))
print("round(11):", round(11))
print("round(123.1):", round(123.1))
print("round(14.547):", round(14.547))

# the output will be...

# round(math.pi, 2): 3.14
# round(math.pi, 4): 3.1416
# round(11): 11
# round(123.1): 123
# round(14.547): 15